        <h2>About this project</h2> 
        <p>The website project MangaMania will allow the user to create an account to keep record of the mangas and animes they are currently seen, so they do
      not lose track of the chapter or episode they are in. It will also notify the user when a new chapter or episode of an ongoing manga or anime is released. The website will also allow the user to rate each manga and anime when they are done reading it.
      In the website, there will be a database with the information of each manga that will allow users to find new mangas to read by name or gender. The database will direct users to external websites to read the manga.
      The website project MangaMania will not host the mangas or animes, it will be used like a link to mangas and animes to keep record of mangas and animes that the user is interested in. And the rate each user gives to a manga and anime will help the community of the 
      website to choose a manga or anime. No option for comments will be allowed to avoid spam, and spoilers.</p>
